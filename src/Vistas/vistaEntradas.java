    private void btn_anadirItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_anadirItemActionPerformed

        String id = txt_codigo.getText();
        String item = txt_item.getText();
        String cantidad = txt_cantidad.getText();
        String usuario = lbl_usuario.getText();

        int fila = this.tbl_items.getSelectedRow();
        String disponible = (String) tbl_items.getValueAt(fila, 3);

        if (Integer.parseInt(cantidad) <= 0) {
            alertaInfo alerta = new alertaInfo("Verifique los datos \n"
                    + "la entrada no puede ser 0");
            alerta.setVisible(true);
        } else {
            modeloEntradas entradas = new modeloEntradas(id, item, cantidad);
            listaEntradas.add(entradas);

            cargarTablaEntradas();
            llenarVista(usuario);
        }

    }//GEN-LAST:event_btn_anadirItemActionPerformed

    private void btn_crearEntradaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearEntradaActionPerformed

        controlEntradas control = new controlEntradas();
        modeloEntradas modelo = new modeloEntradas();

        control.crearDocumentoEntrada(lbl_entrada.getText(), lbl_usuario.getText());
        control.crearEntradas(lbl_entrada.getText(), listaEntradas);
        control.crearPDF(lbl_entrada.getText(), lbl_usuario.getText(), listaEntradas);
        listaEntradas.clear();
        listaEntradas.removeAll(listaEntradas);
        llenarVista(lbl_usuario.getText());
        cargarTabla(buscar);
        modelo = null;
        System.gc();
        cargarTablaEntradas();

    }//GEN-LAST:event_btn_crearEntradaActionPerformed

