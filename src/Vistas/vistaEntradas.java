    private void cargarTabla(String buscar) {

        controlItems control = new controlItems();
        List<modeloItems> items = control.Consultar(buscar);
        DefaultTableModel tabla = new DefaultTableModel();
        this.tbl_items.setModel(tabla);

        tabla.addColumn("Id");
        tbl_items.getColumnModel().getColumn(0).setPreferredWidth(30);
        tbl_items.getColumnModel().getColumn(0).setResizable(false);

        tabla.addColumn("Item");
        tbl_items.getColumnModel().getColumn(1).setPreferredWidth(150);
        tbl_items.getColumnModel().getColumn(1).setResizable(false);

        tabla.addColumn("Descripcion");
        tbl_items.getColumnModel().getColumn(2).setPreferredWidth(30);
        tbl_items.getColumnModel().getColumn(2).setResizable(false);

        tabla.addColumn("Disponible");
        tbl_items.getColumnModel().getColumn(3).setPreferredWidth(30);
        tbl_items.getColumnModel().getColumn(3).setResizable(false);

        for (modeloItems item : items) {
            Object[] fila = new Object[4];
            fila[0] = item.getId();
            fila[1] = item.getNombre();
            fila[2] = item.getDescripcion();
            fila[3] = item.getDisponible();
            tabla.addRow(fila);
        }
    }

    private void llenarVista(String usuario) {

        controlEntradas control = new controlEntradas();
        lbl_entrada.setText(control.contar());

        DateTimeFormatter fecha = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        lbl_fecha.setText(fecha.format(LocalDateTime.now()));

        lbl_usuario.setText(usuario);
        txt_codigo.setText("");
        txt_item.setText("");
        txt_buscar.setText("");
        txt_cantidad.setText("0");

    }

    private void cargarTablaEntradas() {

        DefaultTableModel tabla = new DefaultTableModel();
        this.tbl_entradas.setModel(tabla);

        tabla.addColumn("Id");
        tbl_entradas.getColumnModel().getColumn(0).setPreferredWidth(30);
        tbl_entradas.getColumnModel().getColumn(0).setResizable(false);

        tabla.addColumn("Item");
        tbl_entradas.getColumnModel().getColumn(1).setPreferredWidth(150);
        tbl_entradas.getColumnModel().getColumn(1).setResizable(false);

        tabla.addColumn("Cantidad");
        tbl_entradas.getColumnModel().getColumn(2).setPreferredWidth(30);
        tbl_entradas.getColumnModel().getColumn(2).setResizable(false);

        for (modeloEntradas entrada : listaEntradas) {
            Object[] fila = new Object[3];
            fila[0] = entrada.getId();
            fila[1] = entrada.getInventario();
            fila[2] = entrada.getCantidad();
            tabla.addRow(fila);
        }
    }
