    private void cargarTabla(String buscar) {

        controlItems control = new controlItems();
        List<modeloItems> items = control.Consultar(buscar);
        DefaultTableModel tabla = new DefaultTableModel();
        this.jTable1.setModel(tabla);

        tabla.addColumn("Id");
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTable1.getColumnModel().getColumn(0).setResizable(false);

        tabla.addColumn("Item");
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(150);
        jTable1.getColumnModel().getColumn(1).setResizable(false);

        tabla.addColumn("Descripcion");
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(30);
        jTable1.getColumnModel().getColumn(2).setResizable(false);

        tabla.addColumn("Categoria");
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(3).setResizable(false);

        tabla.addColumn("Disponible");
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(30);
        jTable1.getColumnModel().getColumn(4).setResizable(false);

        for (modeloItems item : items) {
            Object[] fila = new Object[5];
            fila[0] = item.getId();
            fila[1] = item.getNombre();
            fila[2] = item.getDescripcion();
            fila[3] = item.getCategoria();
            fila[4] = item.getDisponible();
            tabla.addRow(fila);
        }
    }

    private void llenarVista() {
        controlItems control = new controlItems();
        lbl_codigo.setText(control.contar());

        List<modeloCategorias> categorias = control.categorias();
        box_categoria.removeAllItems();
        for (int i = 0; i < categorias.size(); i++) {
            box_categoria.addItem(categorias.get(i).getNombre());
        }

        txt_nombre.setText("");
        area_descripcion.setText("");

    }
