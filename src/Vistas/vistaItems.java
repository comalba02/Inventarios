    private void btn_crearItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearItemActionPerformed

        String id = lbl_codigo.getText();
        String nombre = txt_nombre.getText();
        String descripcion = area_descripcion.getText();
        String categoria = (String) box_categoria.getSelectedItem();

        if (nombre.equals("") && descripcion.equals("")) {
            alertaInfo alerta = new alertaInfo("Por favor verifique los datos.");
            alerta.setVisible(true);
        } else {
            modeloItems modelo = new modeloItems(id, nombre, descripcion, categoria);
            controlItems control = new controlItems();
            if (control.crearItem(modelo)) {
                txt_nombre.setText("");
                area_descripcion.setText("");
            }

        }
        llenarVista();
        cargarTabla(buscar);
    }//GEN-LAST:event_btn_crearItemActionPerformed

    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        
        if (rol.equals("1")) {
            int fila = this.jTable1.getSelectedRow();
            lbl_codigo.setText((String) jTable1.getValueAt(fila, 0));
            txt_nombre.setText((String) jTable1.getValueAt(fila, 1));
            area_descripcion.setText((String) jTable1.getValueAt(fila, 2));
            box_categoria.setSelectedItem(jTable1.getValueAt(fila, 3));
        } else {
            alertaInfo alerta = new alertaInfo("No tiene asignado el rol de \nadministrador.");
            alerta.setVisible(true);
        }

    }//GEN-LAST:event_EditarActionPerformed

