    private void btn_crearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearUsuarioActionPerformed

        String id = lbl_codigo.getText();
        String empresa = "1";
        String empleado = (String) box_empleado.getSelectedItem();
        String rol = (String) box_rol.getSelectedItem();
        String usuario = txt_usuario.getText();
        String contrasena = new String(txt_contrasena.getPassword());
        if (usuario.equals("") && contrasena.equals("")) {
            alertaInfo alerta = new alertaInfo("Por favor verifique los datos.");
            alerta.setVisible(true);
        } else {
            modeloUsuarios modelo = new modeloUsuarios(id, empleado, rol, usuario, contrasena);
            controlUsuarios control = new controlUsuarios();
            if (control.crearUsuario(modelo)) {
                txt_usuario.setText("");
                txt_contrasena.setText("");
            }

        }
        llenarVista();
        cargarTabla(buscar);

    }//GEN-LAST:event_btn_crearUsuarioActionPerformed

