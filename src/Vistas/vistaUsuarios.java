    private void llenarVista() {

        controlUsuarios controlU = new controlUsuarios();
        lbl_codigo.setText(controlU.contar());

        List<modeloEmpleados> empleados = controlU.empleados();
        box_empleado.removeAllItems();
        for (int i = 0; i < empleados.size(); i++) {
            box_empleado.addItem(empleados.get(i).getNombre());
        }

        controlRoles controlR = new controlRoles();
        List<modeloRoles> roles = controlR.roles();
        box_rol.removeAllItems();
        for (int i = 0; i < roles.size(); i++) {
            box_rol.addItem(roles.get(i).getNombre());
        }

        txt_usuario.setText("");
        txt_contrasena.setText("");
    }

    private void cargarTabla(String buscar) {

        controlUsuarios control = new controlUsuarios();
        List<modeloUsuarios> usuarios = control.Consultar(buscar);
        DefaultTableModel tabla = new DefaultTableModel();
        this.jTable1.setModel(tabla);

        tabla.addColumn("Id");
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTable1.getColumnModel().getColumn(0).setResizable(false);

        tabla.addColumn("Empleado");
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(150);
        jTable1.getColumnModel().getColumn(1).setResizable(false);

        tabla.addColumn("Usuario");
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(30);
        jTable1.getColumnModel().getColumn(2).setResizable(false);

        tabla.addColumn("Rol");
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(3).setResizable(false);

        for (modeloUsuarios usuario : usuarios) {
            Object[] fila = new Object[4];
            fila[0] = usuario.getId();
            fila[1] = usuario.getEmp();
            fila[2] = usuario.getUsuario();
            fila[3] = usuario.getRol();
            tabla.addRow(fila);
        }

    }

