    private void llenarVista() {

        controlEmpleados control = new controlEmpleados();
        lbl_codigo.setText(control.contar());
        lbl_empresa.setText(control.empresa());

        box_tipoDoc.removeAllItems();
        List<modeloDocumentos> docTipos = control.tipoDoc();
        for (int i = 0; i < docTipos.size(); i++) {
            box_tipoDoc.addItem(docTipos.get(i).getTipo());
        }

        txt_numDocumento.setText("");
        txt_nombre.setText("");
        txt_correo.setText("");
    }

    private void cargarTabla(String buscar) {

        controlEmpleados control = new controlEmpleados();
        List<modeloEmpleados> empleados = control.Consultar(buscar);
        DefaultTableModel tabla = new DefaultTableModel();
        this.jTable1.setModel(tabla);

        tabla.addColumn("Id");
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(30);
        jTable1.getColumnModel().getColumn(0).setResizable(false);

        tabla.addColumn("Empresa");
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(1).setResizable(false);

        tabla.addColumn("Tipo Documento");
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(2).setResizable(false);

        tabla.addColumn("Documento");
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(60);
        jTable1.getColumnModel().getColumn(3).setResizable(false);

        tabla.addColumn("Nombre");
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(200);
        jTable1.getColumnModel().getColumn(4).setResizable(false);

        tabla.addColumn("Genero");
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(60);
        jTable1.getColumnModel().getColumn(5).setResizable(false);

        tabla.addColumn("Correo");
        jTable1.getColumnModel().getColumn(6).setPreferredWidth(100);
        jTable1.getColumnModel().getColumn(6).setResizable(false);

        for (modeloEmpleados empleado : empleados) {
            Object[] fila = new Object[7];
            fila[0] = empleado.getId();
            fila[1] = empleado.getEmpresa();
            fila[2] = empleado.getTipoDoc();
            fila[3] = empleado.getDocumento();
            fila[4] = empleado.getNombre();
            if (empleado.getGenero().equals("M")) {
                fila[5] = "Masculino";

            } else {
                fila[5] = "Femenino";
            }
            fila[6] = empleado.getCorreo();
            tabla.addRow(fila);
        }

    }

    private boolean verificarCorreo(String correo) {

        Pattern pat = null;
        Matcher mat = null;
        pat = Pattern.compile("^[\\w\\\\\\+]+(\\.[\\w\\\\]+)*@([A-Za-z0-9-]+\\.)+[A-Za-z]{2,4}$");
        mat = pat.matcher(correo);
        if (mat.find()) {
            return true;
        } else {
            return false;
        }
    }

