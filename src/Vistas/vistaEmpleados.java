    private void btn_crearEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearEmpleadoActionPerformed

        String id = lbl_codigo.getText();
        String empresa = "1";
        String tipoDoc = (String) box_tipoDoc.getSelectedItem();
        String documento = txt_numDocumento.getText();
        String nombre = txt_nombre.getText();
        String genero = (String) box_genero.getSelectedItem();
        String correo = txt_correo.getText();

        if (documento.equals("") || nombre.equals("")) {
            alertaInfo alerta = new alertaInfo("Por favor verifique los datos.");
            alerta.setVisible(true);
        } else if (verificarCorreo(correo) == false) {
            alertaInfo alerta = new alertaInfo("Correo invalido.");
            alerta.setVisible(true);
        } else {
            modeloEmpleados modelo = new modeloEmpleados(id, empresa, tipoDoc, documento, nombre, genero, correo);
            controlEmpleados control = new controlEmpleados();
            if (control.crearEmpleado(modelo)) {
                txt_numDocumento.setText("");
                txt_nombre.setText("");
                txt_correo.setText("");
                llenarVista();
                cargarTabla(buscar);
            }
        }


    }//GEN-LAST:event_btn_crearEmpleadoActionPerformed

